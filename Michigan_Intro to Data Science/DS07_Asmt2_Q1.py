# Question 1 - Which country has won the most gold medals in summer games? 

summer_gold = pd.Series(df['Gold']).sort_values()
sg_size = summer_gold.shape
most_golds = int(sg_size[0]) - 1
print('The country with the most gold medals in the summer games is: ', summer_gold.index[most_golds])

